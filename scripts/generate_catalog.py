def main() -> None:
    chronic = [
        "сахарный диабет",
        "гипертония",
        "язвенная болезнь",
        "хроническая болезнь печени",
        "хроническая болезнь почек",
    ]
    allergies = ["НПВС", "пенициллины", "лактоза", "глютен", "ацетилсалициловая кислота"]

    catalog: list[dict] = []

    for idx, inn in enumerate(PROFILES.keys()):
        form, dosages_raw = PROFILES[inn]
        dosages = list(dosages_raw)[:MAX_DOSES]
        base_prices = [120 + idx * 31 + i * 19 for i in range(len(dosages))]

        indications = sorted(set(INN_SYMPTOMS[inn]))

        item = {
            "id": _slug(inn),
            "name": _inn_title(inn),
            "form": form,
            "dosage": dosages[0],
            "dosages": dosages,
            "formsAvailable": [form],
            "pricesRub": base_prices,
            "priceRub": float(base_prices[0]),
            "activeSubstances": [inn],
            "indicationsSymptoms": indications,
            "contraindications": sorted(
                {
                    "гиперчувствительность",
                    chronic[idx % len(chronic)],
                }
            ),
            "chronicConditionWarnings": [chronic[(idx + 1) % len(chronic)]],
            "allergyWarnings": [allergies[idx % len(allergies)]],
            "sideEffects": ["тошнота (редко)", "головокружение (редко)"],
            "minAge": 18 if inn in RX_REQUIRED else (12 if idx % 3 == 0 else 6),
            "pregnancyContraindicated": inn in PREGNANCY_RISK,
            "prescriptionRequired": inn in RX_REQUIRED,
        }
        if inn in RX_REQUIRED:
            item["notes"] = "Требуется рецепт врача."

        catalog.append(item)

    target = Path(__file__).resolve().parents[1] / "mobile" / "assets" / "drugs.json"
    target.write_text(json.dumps(catalog, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"written {len(catalog)} drugs to {target}")


if __name__ == "__main__":
    main()
